//Метод, позволяющий распечатать наш массив
void PrintImage (int[,] image)
{
    for (int rows2 = 0; rows2 < image.GetLength(0); rows2 = rows2 + 1)
    {
        for (int columns2 = 0; columns2 < image.GetLongLength(1); columns2 = columns2 + 1)
        {
            //Console.Write($"{image[rows2, columns2]} ");
            if(image[rows2, columns2] == 0) Console.Write(" ");
            else
            {
                Console.Write("+");
            }
        }
            Console.WriteLine();
    }
}


//Метод, позволяющий закрасить нашу картинку, которая была выведена на экран ранее
void FillImage(int row, int col)
{
    if (pic[row, col] == 0)
    {
        pic[row, col] = 1;
        FillImage(row-1, col);
        FillImage(row, col-1);
        FillImage(row+1, col);
        FillImage(row, col+1);
    }
}

